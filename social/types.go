package social

type (
	// FacebookUsername is a username on facebook. It can be a user-defined name
	// like joe.smith or it could be a Facebook generated name, as with a public
	// page like Joe-Smith-Magician-129875824511529
	FacebookUsername string
	// InstagramHandle is a user's handle on Instagram. It does not include the
	// URL or a leading @.
	InstagramHandle string
	// TwitterHandle is a user's handle on Twitter. It does not include the
	// URL or a leading @.
	TwitterHandle string
)
